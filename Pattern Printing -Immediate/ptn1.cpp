#include <bits/stdc++.h>
using namespace std;

int main()
{
        int r = 4, c = 4;
        for (int i = 1; i <= r; i++)
        {
                for (int j = 1; j <= c; j++)
                {
                        cout << "* ";
                }
                cout << endl;
        }
        return 0;
}